/*
Questão 7: Como você pode declarar e atribuir um valor a uma variável em uma única linha em JavaScript?
*/

// Usando let
let myVariable = 10;

// Usando const
const PI = 3.14;

// Usando var
var x = "Hello";

// Você também pode atribuir valores a múltiplas variáveis em uma única linha:
let a = 5, b = 10, c = "JavaScript";
